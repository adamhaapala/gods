package com.dogonfire.gods.tasks;

import com.dogonfire.gods.Gods;
import com.dogonfire.gods.QuestManager;

public class GenerateGlobalQuestTask implements Runnable
{
	private Gods	plugin;

	public GenerateGlobalQuestTask(Gods instance)
	{
		plugin = instance;
	}

	public void run()
	{
		plugin.getQuestManager().generateGlobalQuest();
	}
}
